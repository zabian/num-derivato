package web;

public class CountBean {
	protected String wyr;
	protected String x;
	private	  String h;
	protected String iloraz;
	protected String pochodna;
	
	
	public String getWyr() {
		return wyr;
	}
	public void setWyr(String wyr) {
		this.wyr = wyr;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getH() {
		return h;
	}
	public void setH(String h) {
		this.h = h;
	}
	public String getIloraz() {
		return iloraz;
	}
	public void setIloraz(String iloraz) {
		this.iloraz = iloraz;
	}
	public String getPochodna() {
		return pochodna;
	}
	public void setPochodna(String pochodna) {
		this.pochodna = pochodna;
	}
		
}
